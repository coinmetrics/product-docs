def parse_args(argv: Iterable[str]) -> argparse.Namespace:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--keep-staging",
        action="store_true",
        help="Do not delete .docs-build after the build (useful for debugging).",
    )
    parser.add_argument(
        "--output",
        type=Path,
        default=DEFAULT_OUTPUT_ROOT,
        help=(
            "Directory the rendered GitBook space is written to. Defaults to "
            "spaces/api-client-python/docs."
        ),
    )
    parser.add_argument(
        "--check",
        action="store_true",
        help=(
            "Build into a scratch directory and diff against --output. Exit "
            "non-zero if the committed output is not byte-for-byte identical "
            "to what the pipeline would produce. Used by CI to ensure every "
            "iteration of the GitBook space is reproducible from the script."
        ),
    )
    return parser.parse_args(list(argv))


def build(output_root: Path) -> None:
    """Run the full pipeline, writing the rendered space into ``output_root``."""
    print(f"Staging source from {SUBMODULE_DOCS_SOURCE} -> {STAGED_SOURCE}")
    stage_source()
    print("Regenerating per-endpoint group pages")
    regenerate_group_pages()
    print(f"Running sphinx-build (markdown) -> {STAGED_BUILD}")
    run_sphinx()
    print(f"Resetting {output_root} (preserving {sorted(PRESERVED_PATHS)})")
    reset_output(output_root)
    print("Copying generated Markdown into the GitBook space")
    collect_outputs(output_root)
    print("Copying static image assets")
    copy_assets(output_root)
    print("Post-processing generated Markdown for GitBook")
    post_process_outputs(output_root)
    print("Writing SUMMARY.md")
    write_summary(output_root)


def _diff_trees(expected: Path, actual: Path) -> List[str]:
    """Return a list of human-readable lines describing differences.

    The two trees are compared by file content (not mtime). Files matching
    ``PRESERVED_PATHS`` are skipped because they live outside the build's
    scope. Returns an empty list if the trees are identical.
    """
    diffs: List[str] = []

    def _walk(rel: Path) -> None:
        exp_dir = expected / rel
        act_dir = actual / rel
        cmp = filecmp.dircmp(exp_dir, act_dir)
        for name in sorted(cmp.left_only):
            if rel == Path(".") and name in PRESERVED_PATHS:
                continue
            diffs.append(f"  only in committed output: {(rel / name).as_posix()}")
        for name in sorted(cmp.right_only):
            if rel == Path(".") and name in PRESERVED_PATHS:
                continue
            diffs.append(f"  only in fresh build:       {(rel / name).as_posix()}")
        # ``filecmp.dircmp`` defaults to a shallow comparison (mtime + size);
        # rerun with full content comparison for the candidate matches.
        match, mismatch, errors = filecmp.cmpfiles(
            exp_dir, act_dir, cmp.common_files, shallow=False
        )
        for name in sorted(mismatch + errors):
            if rel == Path(".") and name in PRESERVED_PATHS:
                continue
            diffs.append(f"  differs: {(rel / name).as_posix()}")
        for name in sorted(cmp.common_dirs):
            _walk(rel / name)

    _walk(Path("."))
    return diffs


def _show_text_diff(expected: Path, actual: Path, rel: str, max_lines: int = 60) -> None:
    """Print a unified diff for a single mismatched file (best-effort)."""
    try:
        result = subprocess.run(
            ["diff", "-u", str(expected / rel), str(actual / rel)],
            capture_output=True,
            text=True,
        )
    except FileNotFoundError:
        return
    out = result.stdout.splitlines()
    if not out:
        return
    print(f"\n--- diff for {rel} (truncated to {max_lines} lines) ---")
    for line in out[:max_lines]:
        print(line)
    if len(out) > max_lines:
        print(f"... ({len(out) - max_lines} more lines)")


def main(argv: Iterable[str]) -> int:
    args = parse_args(argv)

    if args.check:
        committed = args.output.resolve()
        if not committed.is_dir():
            raise SystemExit(
                f"--check requires the committed output at {committed} to exist."
            )
        # Build into a scratch directory under STAGING_ROOT so we can diff
        # against the committed tree without touching it.
        _wipe(CHECK_OUTPUT)
        CHECK_OUTPUT.mkdir(parents=True)
        # Copy preserved files into the scratch output so the diff ignores
        # them (otherwise they would show up as "only in committed output").
        for name in PRESERVED_PATHS:
            src = committed / name
            if src.is_file():
                shutil.copy2(src, CHECK_OUTPUT / name)
            elif src.is_dir():
                shutil.copytree(src, CHECK_OUTPUT / name)

        build(CHECK_OUTPUT)

        diffs = _diff_trees(committed, CHECK_OUTPUT)
        if not diffs:
            if not args.keep_staging:
                _wipe(STAGING_ROOT)
            print(f"OK: {committed} matches the output of the build pipeline.")
            return 0

        print(
            f"\nERROR: {committed} is out of sync with the build pipeline.\n"
            "Run `make docs-python-client` (or `python3 scripts/"
            "build_api_client_python_docs.py`) and commit the result.\n"
            "\nSummary of differences:"
        )
        for line in diffs:
            print(line)
        # Show a unified diff for the first few mismatched text files so
        # CI logs make the cause obvious.
        text_mismatches = [
            d.split(": ", 1)[1] for d in diffs if d.lstrip().startswith("differs:")
        ]
        for rel in text_mismatches[:3]:
            if rel.endswith((".md", ".yaml", ".txt")):
                _show_text_diff(committed, CHECK_OUTPUT, rel)
        return 1

    build(args.output)

    if not args.keep_staging:
        _wipe(STAGING_ROOT)

    print(f"Done. Output at {args.output}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main(sys.argv[1:]))
