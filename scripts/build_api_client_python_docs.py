def reset_output(output_root: Path) -> None:
    """Wipe ``output_root`` so the build starts from a known clean state.

    Only ``PRESERVED_PATHS`` survive (``.gitbook.yaml`` is kept because it
    pins the GitBook space configuration and is not produced by the build).
    Everything else, including ``SUMMARY.md`` and ``.gitbook/assets/``, is
    removed up front so a previous run cannot leave stale files behind.
    """
    if not output_root.exists():
        output_root.mkdir(parents=True)
        return
    for entry in output_root.iterdir():
        if entry.name in PRESERVED_PATHS:
            continue
        if entry.is_dir():
            shutil.rmtree(entry)
        else:
            entry.unlink()


def collect_outputs(output_root: Path) -> None:
    """Move/rename generated Markdown files into ``output_root``."""
    if not STAGED_BUILD.is_dir():
        raise SystemExit(f"Sphinx build directory missing: {STAGED_BUILD}")

    # Slug prefixes whose per-method/per-class child pages should keep
    # underscores in their filenames (Python identifiers map 1:1 to the
    # method names in the URL).
    underscore_preserving_prefixes = (
        "reference/groups/",
        f"reference/{EXCEPTIONS_SLUG}/",
    ) + tuple(f"reference/{slug}/" for slug, *_ in EXTRA_CLASS_PAGES)

    for src in STAGED_BUILD.rglob("*.md"):
        rel = src.relative_to(STAGED_BUILD).as_posix()
        target_rel = PAGE_RENAMES.get(rel)
        if target_rel is None:
            if rel.startswith("reference/groups/"):
                target_rel = rel.replace(
                    "reference/groups/", "reference/coinmetricsclient/", 1
                )
            elif any(rel.startswith(p) for p in underscore_preserving_prefixes):
                target_rel = rel
            else:
                target_rel = rel.replace("_", "-")
        target = output_root / target_rel
        target.parent.mkdir(parents=True, exist_ok=True)
        shutil.copy2(src, target)


def copy_assets(output_root: Path) -> None:
    """Mirror static images into the GitBook assets folder.

    The assets directory is wiped first so images removed upstream do not
    linger in the committed output.
    """
    src_dir = SUBMODULE_DOCS_SOURCE / "_static" / "images"
    assets_dir = output_root / GITBOOK_ASSETS_REL
    if assets_dir.exists():
        shutil.rmtree(assets_dir)
    if not src_dir.is_dir():
        return
    assets_dir.mkdir(parents=True, exist_ok=True)
    for image in src_dir.iterdir():
        if image.is_file():
            shutil.copy2(image, assets_dir / image.name)


