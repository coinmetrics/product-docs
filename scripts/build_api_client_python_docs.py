# ``sphinx_markdown_builder`` emits the Sphinx ``:returns:`` and ``:rtype:``
# field-list entries as two separate top-level bullets:
#
#     * **Returns:**
#       <description>
#     * **Return type:**
#       <type>
#
# Both are part of the same logical "what does this method return?"
# block, so combine them into a single ``Returns`` heading with the
# return type as a top-level bullet and the description (if any) as a
# nested bullet underneath. Also handles the (much more common) case
# where only the ``Return type`` bullet is present.
_RETURNS_BLOCK_RE = re.compile(
    r"(?:^\* \*\*Returns:\*\*\n[ \t]+(?P<desc>[^\n]+)\n)?"
    r"^\* \*\*Return type:\*\*\n[ \t]+(?P<type>[^\n]+)",
    re.MULTILINE,
)


def _rewrite_returns_block(match: re.Match[str]) -> str:
    desc = match.group("desc")
    rtype = match.group("type")
    # Leading blank line ensures the previous bullet list (Parameters)
    # is closed before the ``**Returns:**`` paragraph starts.
    lines = ["", "**Returns:**", "", f"* {rtype}"]
    if desc:
        lines.append(f"  * {desc}")
    return "\n".join(lines)

# ``sphinx_markdown_builder`` rewrites every cross-reference to use a ``.md``
# extension, including the intersphinx links that resolve to external HTML
# documentation. Detect and fix the external ones.
_INTERSPHINX_HOSTS = (
    "docs.python.org",
    "pandas.pydata.org",
    "numpy.org",
)
_INTERSPHINX_RE = re.compile(
    r"(\]\(https?://(?:" + "|".join(re.escape(h) for h in _INTERSPHINX_HOSTS) + r")[^)\s]*?)\.md(?=[)#])"
)

# Autodoc rendering. ``sphinx-markdown-builder`` emits headings like
#
#     ### *class* coinmetrics.api_client.CoinMetricsClient(api_key='', ...)
#     #### CoinMetricsClient.get_asset_metrics(assets, ...)
#     #### *property* CoinMetricsClient.foo
#
# These have two problems for GitBook:
#
# 1. The full signature ends up in the heading text. GitBook's right-hand
#    "On this page" panel shows headings as anchor links, so a 200-character
#    constructor signature is unusable as navigation.
# 2. Sphinx's intra-doc cross-references resolve to anchors named after the
#    fully-qualified Python path (``coinmetrics.api_client.CoinMetricsClient``),
#    but GitBook slugifies headings into something completely different, so
#    every ``[Foo](page.md#coinmetrics.x.Foo)`` link is silently broken.
#
# We rewrite each autodoc heading into:
#
#     <a id="coinmetrics.api_client.CoinMetricsClient"></a>
#
#     ### *class* CoinMetricsClient
#
#     ```python
#     class coinmetrics.api_client.CoinMetricsClient(
#         api_key='',
#         verify_ssl_certs=True,
#         ...
#     )
#     ```
#
# which gives GitBook a clean short heading, a stable anchor that matches
# Sphinx's cross-link IDs, and the full pydata-style signature underneath.
#
# Method headings (``#### CoinMetricsClient.foo``) are also promoted to H3
# so they appear in GitBook's "On this page" right-hand panel.

# Kinds where the heading payload contains the FULLY-QUALIFIED dotted path
# (sphinx-markdown-builder always emits class/exception/function with the
# module prefix, regardless of ``.. currentmodule::``).
_QUALIFIED_KINDS = frozenset({"class", "exception", "function", "data"})
_AUTODOC_KIND_RE = re.compile(
    r"^(?P<hashes>#{2,5})\s+"
    r"\*(?P<kind>class|exception|function|method|static|staticmethod|"
    r"classmethod|abstractmethod|abstract|property|attribute|data)\*\s+"
    r"(?P<rest>\S.*?)\s*$"
)
# Plain method/function headings without an explicit kind label, e.g.
# ``#### CoinMetricsClient.get_asset_metrics(...)`` or ``#### run(...)``.
# Match identifiers (with optional dotted path) followed by an optional
# parenthesised argument list, but NOT bare prose headings like
# ``## Endpoints``.
_AUTODOC_PLAIN_RE = re.compile(
    r"^(?P<hashes>#{2,5})\s+"
    r"(?P<rest>"
    r"[A-Za-z_]\w*(?:\\?\.[\w\\]+)+(?:\([^\n]*\))?"  # dotted name (+optional args)
    r"|[A-Za-z_]\w*\([^\n]*\)"                        # bare name + args
    r")\s*$"
)


def _unescape_sig(sig: str) -> str:
    """Drop the ``\\*`` / ``\\_`` escapes the markdown builder injects.

    These escapes are needed in plain Markdown so ``**kwargs`` does not
    render as bold, but inside a code block they would render literally.
    """
    return sig.replace("\\*", "*").replace("\\_", "_")


def _split_signature(payload: str) -> Tuple[str, Optional[str]]:
    """Split ``Foo.bar(arg=1, arg2)`` into ``("Foo.bar", "arg=1, arg2")``.

    Returns ``(dotted_name, None)`` if the payload has no parenthesised
    argument list (e.g. property / attribute signatures).
    """
    payload = payload.strip()
    if "(" not in payload:
        return payload, None
    # Match the FIRST opening parenthesis and take everything to the matching
    # closing one. The args may themselves contain balanced parens (default
    # values like ``factory()``); walk the string to find the right close.
    open_idx = payload.index("(")
    depth = 0
    for i, ch in enumerate(payload[open_idx:], start=open_idx):
        if ch == "(":
            depth += 1
        elif ch == ")":
            depth -= 1
            if depth == 0:
                return payload[:open_idx], payload[open_idx + 1 : i]
    # Unbalanced -- fall back to a simple split.
    return payload[:open_idx], payload[open_idx + 1 :].rstrip(") ")


# Human-readable label rendered as the italic kind annotation that
# pydata-sphinx-theme prints to the left of every API signature ("method",
# "property", "classmethod", ...). Maps the canonical autodoc kind
# emitted by sphinx-markdown-builder to the label Sphinx itself uses.
_KIND_LABELS = {
    "class": "class",
    "exception": "exception",
    "function": "function",
    "method": "method",
    "static": "static method",
    "staticmethod": "static method",
    "classmethod": "classmethod",
    "abstract": "abstract method",
    "abstractmethod": "abstract method",
    "property": "property",
    "attribute": "attribute",
    "data": "data",
}


def _format_pydata_signature_block(
    kind: Optional[str],
    prefix: str,
    full_dotted: str,
    args: Optional[str],
) -> List[str]:
    """Return the lines for an italic kind label + a signature code block.

    Mirrors the way pydata-sphinx-theme renders an API signature: a small
    italic annotation (``method``, ``property``, ``classmethod``, ...) sits
    just above a monospace signature block. In Markdown we use an
    italic-emphasis line followed by a python-fenced code block.
    """
    label = _KIND_LABELS.get(kind) if kind else None
    block = _format_signature_block(prefix, full_dotted, args)
    if label:
        return [f"*{label}*", "", block]
    return [block]


def _format_signature_block(prefix: str, full_dotted: str, args: Optional[str]) -> str:
    """Render the ``python``-fenced signature block printed under each heading."""
    head = f"{prefix} {full_dotted}".strip()
    if args is None:
        body = head
    else:
        # Split args on top-level commas (so ``Dict[str, int]`` stays intact)
        # and put each on its own line for readability.
        pieces: List[str] = []
        depth = 0
        current = ""
        for ch in args:
            if ch in "([{":
                depth += 1
            elif ch in ")]}":
                depth -= 1
            if ch == "," and depth == 0:
                pieces.append(current.strip())
                current = ""
            else:
                current += ch
        if current.strip():
            pieces.append(current.strip())
        if len(pieces) <= 1 or sum(len(p) for p in pieces) + len(head) < 80:
            body = f"{head}({args.strip()})"
        else:
            body = head + "(\n    " + ",\n    ".join(pieces) + ",\n)"
    return "```python\n" + body + "\n```"


def _emit_autodoc_heading(
    kind: Optional[str],
    display_name: str,
    anchor: str,
    full_dotted: str,
    args: Optional[str],
    *,
    level_override: Optional[str] = None,
) -> List[str]:
    """Build the multi-line replacement for a single autodoc heading.

    ``display_name`` is the human-readable text rendered inside the heading.
    For class / exception / function it is the short symbol name
    (``CoinMetricsClient``). For method / property / attribute headings it
    is the qualified ``ClassName.symbol`` form -- this matches Sphinx's
    native pydata rendering and, more importantly for GitBook, makes every
    method heading globally unique so it ranks well in the search bar.

    The display name is wrapped in backticks so it renders in a monospace
    "code" face, mirroring the pydata-sphinx-theme look.

    Heading levels are normalised so GitBook's "On this page" right-hand
    panel surfaces them consistently:

    * ``class`` / ``exception`` -> ``##`` (top-level section on the page).
    * Everything else (methods, properties, attributes, functions, data) ->
      ``###`` (visually nested under their parent class on class pages, or
      top-level on group pages where there is no class heading).
    """
    # pydata-sphinx-theme renders the entire autodoc signature as a
    # single inline code-formatted line, e.g.
    # ``class pandas.DataFrame(data=None, index=None, ...)``. The kind
    # keyword sits inside the code span; there is no separate heading,
    # multi-line code block or italic prefix above the signature. We
    # mirror that here as a single ``## `<kind?> <dotted>(<args>)``` H2
    # so the entire signature is the heading, GitBook lists it in the
    # "On this page" panel, and the page contains no redundant
    # multi-line code block beneath it.
    if kind in {"class", "exception"}:
        prefix = kind
    elif kind in {"static", "staticmethod"}:
        prefix = "static"
    elif kind == "classmethod":
        prefix = "classmethod"
    elif kind in {"abstract", "abstractmethod"}:
        prefix = "abstract"
    elif kind == "property":
        prefix = "property"
    else:
        # Plain methods / functions / attributes / data: no kind keyword
        # in the signature, matching how pydata renders ordinary methods
        # (``DataFrame.head(n=5)``).
        prefix = ""

    # Render the signature as a python-fenced code block. This gives the
    # signature syntax highlighting (matching the pydata-sphinx-theme
    # look where the kind keyword and arguments are colorised) and
    # cleanly wraps long parameter lists across multiple lines via
    # ``_format_signature_block``.
    return [
        f'<a id="{anchor}"></a>',
        "",
        _format_signature_block(prefix, full_dotted, args),
    ]


def _resolve_anchor(
    dotted: str,
    kind: Optional[str],
    class_qualifier: Optional[str],
    module_qualifier: Optional[str],
) -> Tuple[str, str]:
    """Figure out the anchor ID and full dotted path for a heading.

    Returns ``(anchor, full_dotted)``.

    * For class/exception/function/data kinds, the heading payload already
      includes the fully-qualified module path; we use it as-is.
    * For method/property/attribute headings on a documented class, the
      payload may be ``Class.method`` (when emitted under a
      ``currentmodule`` directive) or just ``method`` (when emitted by
      ``automethod`` against a class set as the current context). We
      prepend the module qualifier and the class name as needed.
    """
    if kind in _QUALIFIED_KINDS or "coinmetrics" in dotted:
        return dotted, dotted

    parts = dotted.split(".")
    if class_qualifier:
        # ``class_qualifier`` is itself fully qualified (e.g.
        # ``coinmetrics.api_client.CoinMetricsClient``).
        class_short = class_qualifier.rsplit(".", 1)[-1]
        if parts[0] == class_short:
            full = class_qualifier.rsplit(".", 1)[0] + "." + ".".join(parts)
        else:
            full = class_qualifier + "." + ".".join(parts)
        return full, full

    if module_qualifier:
        full = module_qualifier + "." + ".".join(parts)
        return full, full

    # No context to qualify with -- use the dotted name verbatim. This will
    # not match Sphinx's cross-link anchors but at least keeps the page
    # rendering correctly.
    return dotted, dotted


def _is_per_method_page(page_rel: str) -> bool:
    """Identify pages whose H1 already names the symbol they document.

    On these pages ``automethod`` / ``autoexception`` would emit a redundant
    inline heading just below the H1 -- ``_transform_autodoc_headings``
    drops it so only the page title and the signature block remain. Covers:

    * ``reference/coinmetricsclient/<group>/<method>.md`` (CoinMetricsClient
      endpoint methods)
    * ``reference/<class-slug>/<method>.md`` for each entry in
      ``EXTRA_CLASS_PAGES``
    * ``reference/exceptions/<ExceptionClass>.md``
    """
    parts = page_rel.split("/")
    if not (parts and parts[0] == "reference" and parts[-1].endswith(".md")):
        return False
    if parts[-1] == "README.md":
        return False
    if len(parts) == 4 and parts[1] == "coinmetricsclient":
        return True
    if len(parts) == 3:
        slugs = {slug for slug, *_ in EXTRA_CLASS_PAGES}
        slugs.add(EXCEPTIONS_SLUG)
        return parts[1] in slugs
    return False


def _transform_autodoc_headings(text: str, page_rel: str) -> str:
    """Rewrite every autodoc heading on the page (see module-level comment).

    ``page_rel`` is the output-relative path of the file (e.g.
    ``reference/coinmetricsclient/timeseries/get_asset_metrics.md``). It is
    used to (a) seed the class qualifier on pages where Sphinx does not
    emit a ``*class*`` heading (per-method and cm-stream pages) and (b)
    suppress the inline method heading on per-method pages -- those pages
    already carry the method name in the H1 from the RST title.
    """
    # Resolve the per-page default class qualifier. Group pages match by
    # directory prefix; everything else by exact path.
    class_qualifier: Optional[str] = None
    module_qualifier: Optional[str] = None
    for prefix, qualifier in PAGE_DEFAULT_CLASS_QUALIFIER.items():
        if page_rel.startswith(prefix) or page_rel == prefix:
            class_qualifier = qualifier
            module_qualifier = qualifier.rsplit(".", 1)[0]
            break

    # On per-method pages, the page H1 already says
    # ``CoinMetricsClient.<method>``, so the inline H3 produced by
    # ``automethod`` would duplicate it. Strip it down to the signature
    # block + anchor (no heading line).
    suppress_method_heading = _is_per_method_page(page_rel)

    # On per-method / per-exception pages, mirror the layout of the
    # class definition pages (e.g. ``reference/data-collection/README.md``):
    # a plain-text H1 from the RST title, an HTML anchor for cross-links,
    # then a single ``## `<kind> <Class.symbol>``` H2 followed by the
    # python signature block. The transformations below promote the
    # ``automethod`` / ``autoexception`` heading to H2 instead of letting
    # it default to H3, so the in-page outline matches the class pages.
    promote_heading_to_h2 = suppress_method_heading

    out: List[str] = []

    def _append_block(block: List[str]) -> None:
        # Ensure a blank line precedes the inline HTML anchor; without it
        # CommonMark may glue the anchor onto the previous heading or
        # paragraph, suppressing the rewritten heading entirely.
        if out and out[-1].strip():
            out.append("")
        out.extend(block)

    def _qualified_method_name(dotted: str) -> str:
        """``Class.method`` form for use as the displayed heading text."""
        short = dotted.rsplit(".", 1)[-1]
        if class_qualifier:
            class_short = class_qualifier.rsplit(".", 1)[-1]
            # Avoid double-prefixing if Sphinx already emitted ``Class.method``.
            if dotted.startswith(class_short + "."):
                return dotted
            return f"{class_short}.{short}"
        return short

    for line in text.split("\n"):
        m = _AUTODOC_KIND_RE.match(line)
        if m:
            kind = m.group("kind")
            payload = _unescape_sig(m.group("rest"))
            dotted, args = _split_signature(payload)
            if kind in {"class", "exception"}:
                # Reset the class context for subsequent method headings.
                class_qualifier = dotted
                module_qualifier = dotted.rsplit(".", 1)[0]
                display = dotted.rsplit(".", 1)[-1]
                anchor, full = dotted, dotted
            elif kind == "function":
                # Module-level functions have no enclosing class to qualify
                # with; render them with their short name.
                display = dotted.rsplit(".", 1)[-1]
                anchor, full = dotted, dotted
            else:
                anchor, full = _resolve_anchor(
                    dotted, kind, class_qualifier, module_qualifier
                )
                display = _qualified_method_name(dotted)
            _append_block(
                _emit_autodoc_heading(
                    kind,
                    display_name=display,
                    anchor=anchor,
                    full_dotted=full,
                    args=args,
                    level_override="##" if promote_heading_to_h2 else None,
                )
            )
            continue

        m = _AUTODOC_PLAIN_RE.match(line)
        if m:
            payload = _unescape_sig(m.group("rest"))
            dotted, args = _split_signature(payload)
            anchor, full = _resolve_anchor(
                dotted, None, class_qualifier, module_qualifier
            )
            # Plain headings (no explicit kind label in the source) are
            # emitted by ``automethod`` for ordinary methods, so render
            # them with the ``method`` annotation to match the class
            # pages, where every member has a kind keyword in its
            # heading.
            _append_block(
                _emit_autodoc_heading(
                    kind="method" if promote_heading_to_h2 else None,
                    display_name=_qualified_method_name(dotted),
                    anchor=anchor,
                    full_dotted=full,
                    args=args,
                    level_override="##" if promote_heading_to_h2 else None,
                )
            )
            continue

        out.append(line)

    return "\n".join(out)


def post_process_file(path: Path, output_root: Path) -> None:
    text = path.read_text()
    original = text

    # Belt-and-suspenders: ``sphinx_markdown_builder`` consumes ``{toctree}``
    # and ``{eval-rst}`` blocks itself, but if anything slips through (for
    # example because the staged file failed to parse) we strip it here.
    text = _RESIDUAL_TOCTREE_RE.sub("", text)
    text = _RESIDUAL_EVAL_RST_RE.sub("", text)

    # Restore the correct extension on intersphinx URLs the markdown builder
    # mistakenly rewrote to ``.md``.
    text = _INTERSPHINX_RE.sub(r"\1.html", text)

    # The submodule's reference/coinmetricsclient.md toctree points at
    # ``groups/<slug>``. We relocate those generated pages under
    # ``coinmetricsclient/`` so they nest beneath their parent in the GitBook
    # sidebar, so rewrite the parent page's links to match.
    text = re.sub(r"\]\(groups/", "](coinmetricsclient/", text)

    # ``myst-parser`` strips the URL out of links whose target it cannot
    # resolve (e.g. references to documents we have not staged), leaving
    # ``[Label]()``. GitBook would render that as a broken link, so collapse
    # such patterns down to plain text.
    text = re.sub(r"\[([^\]]+)\]\(\)", r"\1", text)

    # The MyST sources reference logos as ``_static/images/...``. The static
    # tree is not copied into the GitBook output, so rewrite to the assets
    # path that GitBook understands. A leading slash makes the reference
    # absolute relative to the space root, which works from any depth.
    text = _STATIC_IMAGE_RE.sub("/.gitbook/assets/", text)

    try:
        rel_to_root = path.relative_to(output_root).as_posix()
    except ValueError:
        rel_to_root = ""

    # Replace the H1 title for sphinx-apidoc-generated pages. The default is
    # ``# coinmetrics.<module> module`` (with backslash-escaped underscores
    # for private modules), which is awkward and exposes the implementation
    # detail of which file a class lives in.
    desired_title = PAGE_TITLES.get(rel_to_root)
    if desired_title is not None:
        text = re.sub(
            r"\A#\s+coinmetrics(?:\\?\.|\.)\\?[\w\\]+\s+module\s*\n",
            f"# {desired_title}\n",
            text,
        )

    # Drop section headings whose body was removed by earlier processing
    # (e.g. ``## Explore the Docs`` after the grid block beneath it was
    # stripped from the source). Done before the autodoc-heading rewrites
    # below so we do not match against a heading that is about to be
    # wrapped in backticks.
    text = _strip_empty_sections(text)

    # Rewrite Sphinx autodoc headings into a GitBook-friendly form: short
    # heading text (just the symbol name), an HTML anchor matching the
    # Sphinx cross-reference ID, and the full signature in a code block.
    # Only apply to the reference tree -- the narrative pages have ordinary
    # headings we should not touch.
    if rel_to_root.startswith("reference/"):
        text = _transform_autodoc_headings(text, rel_to_root)
        # Combine the separate ``Returns:`` and ``Return type:`` bullets
        # into a single ``Returns`` block with the type as a top-level
        # bullet and the description (when present) nested below.
        text = _RETURNS_BLOCK_RE.sub(_rewrite_returns_block, text)

    # Rewrite intra-doc links that refer to the legacy (underscore or
    # ``index.md``) names of files we have renamed via PAGE_RENAMES.
    # We only need to rewrite the *trailing* portion of the link target so
    # we cover both same-directory references (``data_collection.md``) and
    # parent-relative ones (``reference/api_client.md``).
    #
    # Apply longer source names first so ``parallel_data_collection.md`` is
    # matched before ``data_collection.md`` (which is a suffix of it).
    rename_pairs = sorted(
        PAGE_RENAMES.items(),
        key=lambda item: len(Path(item[0]).name),
        reverse=True,
    )
    for src_rel, target_rel in rename_pairs:
        src_name = Path(src_rel).name
        target_name = Path(target_rel).name
        if src_name == target_name:
            continue
        # Anchor on a ``/`` or ``(`` so we don't slice into longer filenames
        # that happen to end with ``src_name`` (e.g. ``parallel_data_collection``
        # -> ``data_collection``).
        pattern = re.compile(
            r"(\]\((?:[^)#\s]*?[/(])?)" + re.escape(src_name) + r"(?=[)#])"
        )
        text = pattern.sub(lambda m, t=target_name: m.group(1) + t, text)
        # Same-directory bare references like ``](data_collection.md)``.
        text = re.sub(
            r"\]\(" + re.escape(src_name) + r"(?=[)#])",
            f"]({target_name}",
            text,
        )

    if text != original:
        path.write_text(text)


def post_process_outputs(output_root: Path) -> None:
    for md in output_root.rglob("*.md"):
        if md.name == "SUMMARY.md":
            continue
        post_process_file(md, output_root)


def write_group_index_pages(output_root: Path) -> None:
    """Generate the README landing page for every per-method section.

    Three flavours of section are produced:

    * Endpoint groups under ``reference/coinmetricsclient/<slug>/`` --
      the README is fully rewritten because Sphinx renders the staged
      ``index.rst`` into a near-empty page (just the H1 from the title).
      We replace it with a short description plus a bullet list linking
      every method in the group.
    * Per-class sections (``reference/<slug>/`` for DataCollection,
      ParallelDataCollection, CmStream) -- the README produced by Sphinx
      already contains the ``autoclass`` signature block, so we *append*
      a "Methods" list to the existing file rather than overwriting it.
    * Exceptions (``reference/exceptions/``) -- README is rewritten
      with a short intro and a bullet list of exception classes.
    """
    # CoinMetricsClient endpoint groups -- full rewrite.
    for slug, label in GROUP_PAGES:
        methods = GROUP_METHODS.get(slug)
        if not methods:
            continue
        readme = output_root / "reference" / "coinmetricsclient" / slug / "README.md"
        readme.parent.mkdir(parents=True, exist_ok=True)
        lines: List[str] = [f"# {label}", ""]
        lines.append(
            "Endpoint methods on "
            "[`CoinMetricsClient`](../../coinmetricsclient.md) that hit "
            f"the `{slug}` endpoint root."
        )
        lines.append("")
        for method_name in methods:
            lines.append(
                f"* [`CoinMetricsClient.{method_name}`]({method_name}.md)"
            )
        lines.append("")
        readme.write_text("\n".join(lines))

    # Per-class sections -- append a "Methods" list under the autoclass
    # signature so both the class definition and the navigable method
    # list are visible on the landing page.
    for slug, _label, _module, class_name in EXTRA_CLASS_PAGES:
        methods = EXTRA_CLASS_METHODS.get(slug)
        if not methods:
            continue
        readme = output_root / "reference" / slug / "README.md"
        if not readme.is_file():
            continue
        existing = readme.read_text().rstrip()
        appendix: List[str] = ["", "", "### Methods", ""]
        for method_name in methods:
            appendix.append(
                f"* [`{class_name}.{method_name}`]({method_name}.md)"
            )
        appendix.append("")
        readme.write_text(existing + "\n".join(appendix))

    # Exceptions section -- full rewrite.
    if EXCEPTION_CLASSES:
        readme = output_root / "reference" / EXCEPTIONS_SLUG / "README.md"
        readme.parent.mkdir(parents=True, exist_ok=True)
        lines = [f"# {EXCEPTIONS_LABEL}", ""]
        lines.append(
            "The Coin Metrics Python client raises a typed exception "
            "hierarchy for HTTP errors, rate limiting, transport failures "
            "and data-collection errors. All exception classes are "
            "exported from the top-level `coinmetrics` package."
        )
        lines.append("")
        for cls_name in EXCEPTION_CLASSES:
            lines.append(f"* [`{cls_name}`]({cls_name}.md)")
        lines.append("")
        readme.write_text("\n".join(lines))


