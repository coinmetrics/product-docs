#!/usr/bin/env python3
"""Build the api-client-python GitBook space.

This orchestrator wraps Sphinx so that the MyST-Markdown + RST sources in
``submodules/api-client-python/docs/source`` are rendered to GitBook-native
Markdown under ``spaces/api-client-python/docs``.

Every file under ``spaces/api-client-python/docs`` (except the static
``.gitbook.yaml``) is regenerated by this script:

* ``SUMMARY.md`` is composed from ``SUMMARY_CONFIG`` so the sidebar always
  matches the set of pages the build emits.
* The page tree under ``reference/``, ``user-guide/`` and ``releases/`` is
  produced by Sphinx + sphinx-apidoc + the upstream ``_generate_groups.py``.
* ``.gitbook/assets/`` is wiped and re-populated from the submodule's
  ``_static/images/`` directory.

This means a full rebuild is byte-for-byte reproducible from the submodule
SHA pinned in ``submodules/api-client-python``: running
``scripts/build_api_client_python_docs.py --check`` re-runs the whole
pipeline into a scratch directory and exits non-zero if anything differs
from what is committed under ``spaces/api-client-python/docs``.

The pipeline itself works as follows. It deliberately leaves the submodule
untouched and instead:

1. Stages the submodule's ``docs/source`` tree into ``.docs-build/source``.
2. Runs the submodule's ``_generate_groups.py`` to (re)create the per-endpoint
   reference pages, with its module-level ``REPO_ROOT`` and ``OUTPUT_DIR``
   monkey-patched to the staged paths so nothing is written into the submodule
   working tree.
3. Drops in an overlay ``conf.py`` that re-exports the submodule conf and
   appends ``sphinx_markdown_builder`` to ``extensions``.
4. Invokes ``sphinx-build -b markdown`` against the staged source.
5. Post-processes the generated Markdown:
   - rewrites ``.html`` links to ``.md``
   - strips residual ``{toctree}`` and ``{eval-rst}`` fences
   - converts ``{grid-item-card}`` blocks to GitBook-friendly bullet lists
   - rewrites ``_static/images/...`` references to ``.gitbook/assets/...``
   - copies image assets into ``<output>/.gitbook/assets``
6. Writes ``<output>/SUMMARY.md`` from ``SUMMARY_CONFIG``.
"""

