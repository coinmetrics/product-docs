def run_sphinx() -> None:
    _wipe(STAGED_BUILD)
    STAGED_BUILD.mkdir(parents=True)
    cmd = [
        sys.executable,
        "-m",
        "sphinx",
        "-b",
        "markdown",
        "-q",
        str(STAGED_SOURCE),
        str(STAGED_BUILD),
    ]
    print("$ " + " ".join(cmd))
    result = subprocess.run(cmd, cwd=str(REPO_ROOT))
    if result.returncode != 0:
        raise SystemExit(f"sphinx-build exited with {result.returncode}")


